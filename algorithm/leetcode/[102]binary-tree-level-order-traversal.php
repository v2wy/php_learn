/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($val = 0, $left = null, $right = null) {
 *         $this->val = $val;
 *         $this->left = $left;
 *         $this->right = $right;
 *     }
 * }
 */
class Solution
{

    /**
     * @param  TreeNode  $root
     * @return Integer[][]
     */
    function levelOrder($root)
    {
        if ($root == null) {
            return [];
        }
        $queue = [];
        $queue[] = $root;

        $ans = [];
        while ($queue) {
            $queueCount = count($queue);
            $row = [];
            for ($i = 0; $i < $queueCount; $i++) {
                $node = array_shift($queue);
                if ($node->left != null) {
                    $queue[] = $node->left;
                }
                if ($node->right != null) {
                    $queue[] = $node->right;
                }
                $row[] = $node->val;
            }
            $ans[] = $row;
        }
        return $ans;
    }
}

