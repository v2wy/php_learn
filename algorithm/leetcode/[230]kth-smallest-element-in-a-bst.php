/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($val = 0, $left = null, $right = null) {
 *         $this->val = $val;
 *         $this->left = $left;
 *         $this->right = $right;
 *     }
 * }
 */
class Solution
{

    private $ans = 0;
    private $foo = 0;
    private $k;

    /**
     * @param  TreeNode  $root
     * @param  Integer  $k
     * @return Integer
     */
    function kthSmallest($root, $k)
    {
        $this->k = $k;
        $this->travel($root);
        return $this->ans;
    }

    function travel($root)
    {
        if ($root == null) {
            return;
        }
        $this->travel($root->left);
        $this->foo++;
        if ($this->foo == $this->k) {
            $this->ans = $root->val;
            return;
        }
        $this->travel($root->right);
    }
}

