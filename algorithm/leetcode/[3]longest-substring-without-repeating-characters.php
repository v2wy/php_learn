class Solution
{

    /**
     * @param  String  $s
     * @return Integer
     */
    function lengthOfLongestSubstring($s)
    {
        $window = [];
        $left = $right = 0;
        $res = 0;
        $length = strlen($s);
        while($right < $length){
            $rightChar = $s[$right];
            $window[$rightChar] ++;
            $right ++;
            while ($window[$rightChar] > 1){
                $leftChar = $s[$left];
                $window[$leftChar]--;
                $left++;
            }
            $res = max($res, $right - $left);
        }
        return  $res;
    }
}
