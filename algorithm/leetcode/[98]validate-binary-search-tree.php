/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($val = 0, $left = null, $right = null) {
 *         $this->val = $val;
 *         $this->left = $left;
 *         $this->right = $right;
 *     }
 * }
 */
class Solution
{
    private $pre = PHP_INT_MIN;

    /**
     * @param  TreeNode  $root
     * @return Boolean
     */
    function isValidBST($root)
    {
        //è€ƒå¯Ÿä¸­åºéåŽ†çš„æ€§è´¨ BSTçš„ä¸­åºéåŽ†æ˜¯ä¸€ä¸ªæœ‰åºæ•°ç»„
        if ($root == null) {
            return true;
        }
        if (!$this->isValidBST($root->left)) {
            return false;
        }
        if ($root->val <= $this->pre) {
            return false;
        }
        $this->pre = $root->val;

        return $this->isValidBST($root->right);
    }


}

